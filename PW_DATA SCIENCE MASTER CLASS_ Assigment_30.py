# ## Q4. Explain the concept of levels measurement and give an example of a variable for each level.
# 
# ### Ans:-
# 
#         Levels of measurement, also known as scales of measurement.
#         There are four main levels of measurement: 1)nominal, 2)ordinal, 3)interval, and 4)ratio.
#         
#         1.Nominal level:- Nominal level data are categorical data that are used to classify or categorize objects or 
#                           events. This level of measurement involves assigning numbers or labels to items without any
#                           quantitative meaning.
#           
#           Example: Types of fruit such as apples, bananas, and oranges can be classified using nominal level data. 
#                    Each fruit is assigned a label or number, but there is no inherent order or relationship between
#                    them.
#           
#         2.Ordinal level:- Ordinal level data are used to rank or order items based on a particular characteristic or
#                           attribute. This level of measurement involves assigning numbers to items in a way that reflects
#                           their relative position or order.
#                           
#           Example: Ranking students in a class from highest to lowest based on their grade point average is an example of 
#                    ordinal level data. The ranking is based on the order of the students' GPAs, but the difference between
#                    each ranking may not be equal.
#                    
#        3.Interval level:- Interval level data involves the use of numerical data with equal intervals between each value. 
#                           This level of measurement involves assigning numbers to items in a way that reflects their 
#                           relative magnitude and the difference between each value is meaningful.
#                           
#           Example: Measuring temperature in Celsius or Fahrenheit is an example of interval level data. The difference
#                    between 10 and 20 degrees Celsius is the same as the difference between 20 and 30 degrees Celsius.
#              
#        4.Ratio level: Ratio level data involves the use of numerical data with a true zero point. This level of measurement
#                       involves assigning numbers to items in a way that reflects their relative magnitude and the ratio 
#                       between each value is meaningful.
#                       
#          Example: Measuring weight or height is an example of ratio level data. A person's weight of 60 kilograms is twice 
#                   as much as a person who weighs 30 kilograms. Similarly, a person who is 180 centimeters tall is twice as
#                   tall as a person who is 90 centimeters tall.
#                   
