# ## Q.10. What are some common measures of Central tendency and variability used in statistics? Explain how each measure can be used to describe a dataset.
# 
# ### Ans:-
#      Measures of central tendency refer to the typical or average value in a dataset. The most commonly used measures of 
#      central tendency are:
#      
#      1.Mean: The mean is the sum of all values in the dataset divided by the number of values. The mean is a useful measure
#              of central tendency when the data is normally distributed and there are no extreme values (outliers) in the
#              dataset.
#      2.Median: The median is the middle value in a dataset when the data is arranged in order of magnitude. The median is a
#                useful measure of central tendency when the data is skewed or has outliers, as it is not affected by extreme
#                values.
#      3.Mode: The mode is the most frequently occurring value in a dataset. The mode is a useful measure of central tendency
#              when the data is categorical or discrete.
#              
#      Measures of variability, on the other hand, describe how spread out or dispersed the values in a dataset are. The most
#      commonly used measures of variability are:  
#      
#      1.Range: The range is the difference between the maximum and minimum values in a dataset. The range is a useful measure 
#               of variability when the data is evenly distributed.
#               
#      2.Variance: The variance is the average of the squared differences from the mean. The variance measures the variability
#                  of the dataset by comparing each data point to the mean, and then squaring and averaging the differences.
#                  
#      3.Standard deviation: The standard deviation is the square root of the variance. The standard deviation is a useful 
#                            measure of variability because it is expressed in the same units as the data, and it gives an
#                            indication of how tightly the data is clustered around the mean.
