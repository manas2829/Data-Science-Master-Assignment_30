# ## Q7. How Nominal data type is different from Ordinal data type.
# 
# ### Ans:-
# 
#     Nominal data refers to data that can be categorized into distinct categories or groups, without any inherent order or
#     ranking. Examples of nominal data include gender (male/female), race (Asian, Black, Hispanic, White), and favorite color
#     (red, blue, green). Nominal data can be counted and percentages can be calculated, but no meaningful mathematical 
#     operations, such as addition or multiplication, can be performed on nominal data.
#     
#     Ordinal data, on the other hand, is a type of data that can be ranked or ordered based on some criteria or attribute, 
#     but the intervals between categories are not necessarily equal. Examples of ordinal data include rank orders (first,
#     second, third), education level (high school, college, graduate school), and rating scales (poor, fair,good,excellent).
#     Ordinal data can be counted and ranked, and some mathematical operations such as median, mode, and percentiles can be
#     calculated, but not mean or standard deviation.
