# ## Q9. Describe the different between descriptive and inferential statistics.Give an example of each type of statistics and example how they are used.
# 
# ### Ans:-
# 
#     Descriptive statistics refer to methods used to summarize and describe the important features of a dataset, such as its
#     mean, median, mode, standard deviation, range, and frequency distribution. Descriptive statistics are used to provide a 
#     concise and meaningful summary of a dataset, and can be used to identify patterns, trends, and outliers in the data.
# 
#     An example of descriptive statistics is the calculation of the mean and standard deviation of a set of exam scores. 
#     These statistics can be used to describe the overall performance of the students in the exam,and to identify whether 
#     the scores are evenly distributed or skewed towards higher or lower values.
#     
#     Inferential statistics, on the other hand, refer to methods used to make inferences or predictions about a larger 
#     population based on a sample of data. Inferential statistics involve testing hypotheses, estimating population 
#     parameters, and calculating probabilities.
# 
#     An example of inferential statistics is a hypothesis test to determine whether a new drug is effective in treating a 
#     certain condition. A sample of patients is randomly assigned to receive the new drug or a placebo, and their outcomes
#     are compared. Based on the results of the study, researchers can infer whether the new drug is more effective than 
#     the placebo for the larger population of patients with the same condition.
